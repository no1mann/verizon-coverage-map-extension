const origin = '*';

function getFromUrl(urlStr){
    let url = new URL(urlStr);
    let splitPath = url.pathname.split('/');

    console.log(splitPath);

    //api/style
    if(splitPath.length === 3 && splitPath[1] === 'api' && splitPath[2] === 'style'){
      let params = {
          type: 'style',
          fetch: false,
          attr: {}
        };

        return params;
    }

    return {
      type: 'error',
      fetch: false,
      attr: {}
    };
}

//Fetch Event
addEventListener("fetch", event => {
  event.respondWith(handleRequest(event))
})

function getLocalResource(requestInfo){

  if(requestInfo.type === 'style'){
    return createResponse(style);
  }

  return "{}"
}

//Handle Requests
async function handleRequest(event) {
    
    try{
      const request = event.request;

      let requestInfo = getFromUrl(request.url); 

      let response = getLocalResource(requestInfo);

      return response;
    }
    catch (e){
        console.log("Try/Catch Error Occurred: " + e);
        return new Response(null, {
          status: 404,
          statusText: 'An error occurred',
        });
    }
}


function createResponse(body){
  console.log(body);

  var response = new Response(body);

  //Allow Access Across ALL Origins
  response.headers.set("Access-Control-Allow-Origin", origin);

  //Set JsonObject
  response.headers.set("content-type", 'application/json');

  return response;
}

